package com.wygdove.hw.vo;

public class PhysicalCommonVo {
	private String sflag; // sensor flay
	private String stype; // sensor type
	private String phytime; // physical datetime
	private String phyvalue; // physical value
	
	public String getSflag() {
		return sflag;
	}
	public void setSflag(String sflag) {
		this.sflag = sflag;
	}
	public String getStype() {
		return stype;
	}
	public void setStype(String stype) {
		this.stype = stype;
	}
	public String getPhytime() {
		return phytime;
	}
	public void setPhytime(String phytime) {
		this.phytime = phytime;
	}
	public String getPhyvalue() {
		return phyvalue;
	}
	public void setPhyvalue(String phyvalue) {
		this.phyvalue = phyvalue;
	}
	
}
