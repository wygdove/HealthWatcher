package com.wygdove.hw.vo;

public class EarthquakeVo {
	private String eqTime;
	private String eqMag;
	private String eqDepth;
	private String eqDistance;
	private String eqLatitude;
	private String eqLongitude;
	private String eqPlace;
	
	public String getEqTime() {
		return eqTime;
	}
	public void setEqTime(String eqTime) {
		this.eqTime = eqTime;
	}
	public String getEqMag() {
		return eqMag;
	}
	public void setEqMag(String eqMag) {
		this.eqMag = eqMag;
	}
	public String getEqDepth() {
		return eqDepth;
	}
	public void setEqDepth(String eqDepth) {
		this.eqDepth = eqDepth;
	}
	public String getEqDistance() {
		return eqDistance;
	}
	public void setEqDistance(String eqDistance) {
		this.eqDistance = eqDistance;
	}
	public String getEqLatitude() {
		return eqLatitude;
	}
	public void setEqLatitude(String eqLatitude) {
		this.eqLatitude = eqLatitude;
	}
	public String getEqLongitude() {
		return eqLongitude;
	}
	public void setEqLongitude(String eqLongitude) {
		this.eqLongitude = eqLongitude;
	}
	public String getEqPlace() {
		return eqPlace;
	}
	public void setEqPlace(String eqPlace) {
		this.eqPlace = eqPlace;
	}
}
